  // Get all voices in a flat array
  getAllVoices() {
    return [
      ...this.premium,
      ...this.excellent,
      ...this.professional,
      ...this.good,
      ...this.specialty
    ];
  },

  // Get voices by rating
  getByRating(minRating = 3) {
    return this.getAllVoices().filter(v => v.rating >= minRating);
  },

  // Get voices by use case
  getByUseCase(useCase) {
    return this.getAllVoices().filter(v => 
      v.bestFor.some(bf => bf.toLowerCase().includes(useCase.toLowerCase()))
    );
  },

  // Get recommended voices for collections
  getCollectionsVoices() {
    return this.getAllVoices()
      .filter(v => v.bestFor.includes('Collections') || v.bestFor.includes('Sales'))
      .sort((a, b) => b.rating - a.rating);
  },

  // Get voice by ID
  getById(id) {
    return this.getAllVoices().find(v => v.id === id);
  },

  // Get voice by name or alias
  getByName(name) {
    return this.getAllVoices().find(v => 
      v.name.toLowerCase() === name.toLowerCase() ||
      v.alias?.toLowerCase() === name.toLowerCase()
    );
  }
